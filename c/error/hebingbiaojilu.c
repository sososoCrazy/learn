#include <stdio.h>
#include <string.h>

int main()
{
	int len;
	//while(scanf("%d",&len)!=EOF){
	int key[100];
	int mes[100];
	int count=0;
	int tmp1;
	int tmp2;
	
	scanf("%d",&len);
	for(int i=0;i<len;i++)
	{
		scanf("%d %d",&key[i],&mes[i]);
	}
	for(int x=0;x<len;x++)
	{
		for(int y=x+1;y<len;y++)
		{
			if(mes[x]!=0&&mes[y]!=0)
			{
				if(key[y]==key[x])
				{
					mes[x]=mes[x]+mes[y];
					key[y]=0;
					mes[y]=0;
					//printf("%d,,,%d\n",mes[x],mes[y]);
				}
			}
		}
	}
	for(int z=0;z<len;z++)
	{
		if(mes[z]==0)
			continue;
		else
		{
			key[count]=key[z];
			mes[count]=mes[z];
			count++;
		}
		//printf("%d %d\n",key[z],mes[z]);
	}
	for(int m=0;m<count;m++)
	{
		for(int n=0;n<count-m;n++)
		{
			if(key[m]>key[m+1])
			{
				tmp1=key[m];
				key[m]=key[m+1];
				key[m+1]=tmp1;

				tmp2=mes[m];
				mes[m]=mes[m+1];
				mes[m+1]=tmp2;
			}
		}
	}
	for(int o=0;o<count;o++)
	{
		printf("%d %d\n",key[o],mes[o]);
	}
	//printf("%d,%d",key[2],mes[3]);
	//}
	return 0;

}
